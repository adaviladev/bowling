     * @param  \Illuminate\Http\Request $request
     * @param Frame                     $frame
     *
     * @return void
     */
    public function update(Request $request, Frame $frame)
    {
        if ($index = $request->get('index', null)) {
            $ball_throw = BallThrow::where('frame_id', $frame->id)
                     ->where('index', $index)
                     ->first();
            $ball_throw->update(
                         [
                             'pins' => $request->get('pins'),
                         ]
                     );
        }
    }

